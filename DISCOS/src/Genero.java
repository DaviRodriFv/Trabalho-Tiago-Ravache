public class Genero {
    protected String nomeG;

    public Genero (){}

    public Genero(String nome) {
        this.nomeG = nomeG;
    }

    public String getNome() {
        return nomeG;
    }

    public void setNome(String nome) {
        this.nomeG = nomeG;
    }

    public String toString() {
        return "Nome do Genero: " + nomeG;

    }

}
